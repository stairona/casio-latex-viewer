/* Draw a symbol using bitmap data */
void draw_symbol(int x, int y, enum symbol_id sym, color_t color)
{
    if (sym >= SYM_COUNT) return;
    
    const glyph_t *g = &symbol_glyphs[sym];
    
    for (int row = 0; row < g->height; row++) {
        uint8_t byte = g->data[row];
        for (int col = 0; col < 8; col++) {
            if (byte & (0x80 >> col)) {
                dpixel(x + col, y + row, color);
            }
        }
    }
}
